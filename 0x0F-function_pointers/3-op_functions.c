#include "3-calc.h"

/**
 * op_add - function return sum
 *
 * @a: value
 * @b: value
 *
 * Return: return sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function return sub
 *
 * @a: value
 * @b: value
 *
 * Return: return sub
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function return mul
 *
 * @a: value
 * @b: value
 *
 * Return: return mul
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function return div
 *
 * @a: value
 * @b: value
 *
 * Return: return div
*/
int op_div(int a, int b)
{
	return (a / b);

}
/**
 * op_mod - function return mod
 *
 * @a: value
 * @b: value
 *
 * Return: return mod
*/
int op_mod(int a, int b)
{
	return (a % b);
}
